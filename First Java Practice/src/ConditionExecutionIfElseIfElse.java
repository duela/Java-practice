public class ConditionExecutionIfElseIfElse {
    public static void main(String[] args){
        int x = -10;
        if (x < 0){
            System.out.println("Negative number");
        }
        else if (x > 0) {
            System.out.println("Positive Number");
        }
        else {
            System.out.println("Zero" );
        }
    }
}
